// Printing Variables in c and updation of variables

#include<stdio.h>

int main()
{
    int x;
    x = 5;
    printf("The value of x : %d",x);
    printf("\n");
     x = 9;
    printf("The new value of x : %d",x);
    printf("\n");
     x = x+10;
    printf("The value of x after updation : %d",x);
    printf("\n");
    x = x-15;
    printf("The final value of x : %d",x);
    printf("\n");


    return 0;
}